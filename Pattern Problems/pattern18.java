public class pattern18 {
  public static void main(String[] args) {
    for (int i = 0; i < 5; i++) {
      int number = 69 - (5 - 5);
      int start = number - i;
      char a = (char) start;
      for (int j = 0; j <= i; j++) {
        System.out.print(a);
        a++;
      }
      System.out.println();
    }
  }
}
